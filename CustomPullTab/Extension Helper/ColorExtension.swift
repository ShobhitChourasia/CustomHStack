import UIKit

extension UIColor {
    
    class func customBlackColor() -> UIColor{
        return UIColor(red:15/255, green:18/255, blue:31/255, alpha:1)
    }
    
    class func customGreyTextColor() -> UIColor{
        return UIColor(red:105/255, green:125/255, blue:139/255, alpha:1)
    }
    
    class func paleOrangeColor() -> UIColor{
        return UIColor(red:243/255, green:239/255, blue:127/255, alpha:1)
    }
    
    class func customPurpleColor() -> UIColor{
        return UIColor(red:44/255, green:48/255, blue:140/255, alpha:1)
    }
    
    class func customPinkTextColor() -> UIColor{
        return UIColor(red:183/255, green:192/255, blue:152/255, alpha:1)
    }
    
    class func customBrownBgColor() -> UIColor{
        return UIColor(red:52/255, green:38/255, blue:47/255, alpha:1)
    }
    
    class func customPurpleBgColor() -> UIColor{
        return UIColor(red:105/255, green:94/255, blue:126/255, alpha:1)
    }
    
    class func customBlueBgColor() -> UIColor{
        return UIColor(red:71/255, green:84/255, blue:192/255, alpha:1)
    }
    
    class func customGreenBgColor() -> UIColor{
        return UIColor(red:83/255, green:120/255, blue:52/255, alpha:1)
    }
    
    class func customSkyBlueBgColor() -> UIColor{
        return UIColor(red:69/255, green:102/255, blue:182/255, alpha:1)
    }
}
